"""**1. Web Scraping Merchant Metadata**"""

url = "https://raw.githubusercontent.com/greggles/mcc-codes/master/mcc_codes.json"

try:
    response = requests.get(url, timeout=10)
    response.raise_for_status()
    data = response.json()
    mcc_map = {int(item['mcc']): item['edited_description'] for item in data}
except Exception as e:
    print("MCC mapping download failed:", e)
    mcc_map = {}

df_500['MCC'] = pd.to_numeric(df_500['MCC'], errors='coerce')
df_500['MCC Category'] = df_500['MCC'].map(mcc_map).fillna('Other')

