unique_categories = df_500['MCC Category'].dropna().unique()

category_map = {}

for cat in unique_categories:
    assigned = "Other"
    for group, keywords in broad_categories.items():
        if any(word.lower() in str(cat).lower() for word in keywords):
            assigned = group
            break
    category_map[cat] = assigned

df_500['Behavior_Category'] = df_500['MCC Category'].map(category_map).fillna("Other")

