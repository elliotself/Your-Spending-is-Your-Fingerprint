# Work on a copy so original df_clean is unchanged
df_temporal = df_clean.copy()

# 1. Make sure DateTime is datetime
df_temporal['DateTime'] = pd.to_datetime(df_temporal['DateTime'], errors='coerce')

# Remove invalid timestamps
df_temporal = df_temporal.dropna(subset=['DateTime']).copy()

# 2. Keep only 2009–2019
df_temporal['Year_Val'] = df_temporal['DateTime'].dt.year
df_temporal = df_temporal[
    (df_temporal['Year_Val'] >= 2009) &
    (df_temporal['Year_Val'] <= 2019)
].copy()

# 3. Sort by user and time
df_temporal = df_temporal.sort_values(['User', 'DateTime'])

# 4. Compute inter-event time in hours within each user-year
df_temporal['diff'] = (
    df_temporal
    .groupby(['User', 'Year_Val'])['DateTime']
    .diff()
    .dt.total_seconds() / 3600
)

# 5. Burstiness function
def calculate_burstiness(x):
    x = x.dropna()

    # Need enough inter-event gaps for a stable estimate
    if len(x) < 10:
        return np.nan

    mu = x.mean()
    sigma = x.std(ddof=0)

    if (sigma + mu) == 0:
        return np.nan

    return (sigma - mu) / (sigma + mu)

# 6. Yearly burstiness per user
temporal_stats = (
    df_temporal
    .groupby(['User', 'Year_Val'])['diff']
    .apply(calculate_burstiness)
    .reset_index(name='Temporal_B_year')
)

# Optional: drop user-years that returned NaN
temporal_stats = temporal_stats.dropna(subset=['Temporal_B_year']).copy()

# 7. Final user-level burstiness = average across years
burstiness_user = (
    temporal_stats
    .groupby('User', as_index=False)['Temporal_B_year']
    .mean()
    .rename(columns={'Temporal_B_year': 'Temporal_B'})
)

