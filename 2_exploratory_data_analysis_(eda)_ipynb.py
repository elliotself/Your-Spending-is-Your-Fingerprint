# --------------------------------------------------
# HAVERSINE DISTANCE FUNCTION
# --------------------------------------------------
def haversine_dist(lat1, lon1, lat2, lon2):
    R = 6371.0

    phi1 = np.radians(lat1)
    phi2 = np.radians(lat2)

    dphi = np.radians(lat2 - lat1)
    dlambda = np.radians(lon2 - lon1)

    a = np.sin(dphi / 2) ** 2 + np.cos(phi1) * np.cos(phi2) * np.sin(dlambda / 2) ** 2

    return 2 * R * np.arctan2(np.sqrt(a), np.sqrt(1 - a))

# --------------------------------------------------
# 1. BUILD USER ANCHORS FROM MOST FREQUENT PHYSICAL ZIP
# --------------------------------------------------
physical_tx = df_spatial.dropna(subset=['latitude', 'longitude', 'Zip_clean'])

anchor_df = (
    physical_tx
    .groupby(['User', 'Zip_clean'])
    .agg(
        tx_count=('User', 'size'),
        Anchor_Latitude=('latitude', 'first'),
        Anchor_Longitude=('longitude', 'first'),
    )
    .reset_index()
    .sort_values(['User', 'tx_count'], ascending=[True, False])
    .drop_duplicates(subset='User')
    .rename(columns={'Zip_clean': 'Anchor_Zip'})
)

print(f"Users with a physical anchor: {anchor_df['User'].nunique()}")

all_users = df_spatial['User'].nunique()
anchored_users = anchor_df['User'].nunique()
print(f"{all_users - anchored_users} users dropped (no physical transactions)")

# --------------------------------------------------
# 2. MERGE ANCHORS BACK INTO df_spatial
# --------------------------------------------------
df_spatial = df_spatial.merge(
    anchor_df[['User', 'Anchor_Zip', 'Anchor_Latitude', 'Anchor_Longitude']],
    on='User',
    how='left'
)

# Quick check
print(df_spatial[['User', 'Zip_clean', 'Anchor_Zip', 'Anchor_Latitude', 'Anchor_Longitude']].head())

# --------------------------------------------------
# 3. COMPUTE SPATIAL RANGE PER USER
# --------------------------------------------------
results = []

for user_id, group in df_spatial.groupby('User'):

    if len(group) < 2:
        continue

    anchor_lat = group['Anchor_Latitude'].iloc[0]
    anchor_lon = group['Anchor_Longitude'].iloc[0]

    if pd.isna(anchor_lat) or pd.isna(anchor_lon):
        continue

    # Only compute distances for physically located transactions
    phys = group.dropna(subset=['latitude', 'longitude'])

    if len(phys) < 2:
        continue

    distances = haversine_dist(
        phys['latitude'].values,
        phys['longitude'].values,
        anchor_lat,
        anchor_lon
    )

    # Remove extreme outliers
    if len(distances) >= 5:
        cutoff = np.percentile(distances, 99)
        distances = distances[distances <= cutoff]

    if len(distances) < 2:
        continue

    rg = np.sqrt(np.mean(distances ** 2))

    results.append({
        'User': user_id,
        'Year': '2009-2019',
        'Rg_km': rg,
        'Spatial_Log': np.log1p(rg),
        'Transaction_Count': len(distances)
    })

spatial_results = pd.DataFrame(results)

# --------------------------------------------------
# 4. USER-LEVEL SUMMARY
# --------------------------------------------------
rg_user = spatial_results[['User', 'Rg_km', 'Spatial_Log', 'Transaction_Count']].copy()

